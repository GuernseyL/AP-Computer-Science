package Q1;

public class prog122h {
    public static void main(String[] args) {
        System.out.println("Number \t\t Square \t\t Square Root \t\t Cube Root \t\t Fourth Root");
        double num = 1;
        double sqrt;
        double sqr;
        double cube;
        double fourth;
        while (num <= 20) {
            sqrt = Math.sqrt(num);
            sqr = Math.pow(num, 2);
            cube = Math.pow(num, 3);
            fourth = Math.pow(num, 4);
            System.out.println(num + "\t\t" + sqr + "\t\t" + sqrt + "\t\t" + cube + "\t\t" + fourth);
            num ++;
        }
    }
}
