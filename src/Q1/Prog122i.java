public class Prog122i {
    public static void main(String[] args) {
        System.out.println("Number \t\t Cube \t\t Cube Root");
        double num = -25;
        double cbrt;
        double cube;
        while (num <= 25) {
            cbrt = Math.cbrt(num);
            cube = Math.pow(num, 3);
            System.out.println(num + "\t\t" + cube + "\t\t" + cbrt);
            num ++;
        }
    }
}
